print("Demo of basic data types : String")
s="Hello"
t="World"
print("String 1 :",s)
print("String 2 :",t)
d=s +"" +t
print("String Concatenation :",d)
print("Capitalize :",d.capitalize())
print("Converted to upper case :",s.upper())
