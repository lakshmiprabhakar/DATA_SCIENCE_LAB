print("Demo of basic data types : Numbers")
x=3
y=2.5
print("x=",x)
print("y=",y)
print("Data type of variable x :",type(x))
print("Data type of variable y :",type(y))
print("Addition :",x+y)
print("Subtraction:",x-y)
print("Multiplication:",x*y)
print("Exponentiation:",x**2)

